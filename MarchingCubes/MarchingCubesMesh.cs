    private bool AddSphere(Vector3 position, float radius)
    {
        if (position.x + radius < mesh_offset.x - 0.5F*mesh_dimensions.x)
            return false;
        if (position.x - radius > mesh_offset.x + 0.5F*mesh_dimensions.x)
            return false;

        if (position.y + radius < mesh_offset.y - 0.5F*mesh_dimensions.y)
            return false;
        if (position.y - radius > mesh_offset.y + 0.5F*mesh_dimensions.y)
            return false;

        if (position.z + radius < mesh_offset.z - 0.5F*mesh_dimensions.z)
            return false;
        if (position.z - radius > mesh_offset.z + 0.5F*mesh_dimensions.z)
            return false;
        
        sphere_offsets.Add(position);
        sphere_radii.Add(radius);
        return true;
    }

    private float GetDensity(Vector3 position)
    {
        float min_val = float.PositiveInfinity;
        for (int i = 0; i < sphere_offsets.Count; i++)
        {            
            min_val = Math.Min(min_val, (position-sphere_offsets[i]).Length() - sphere_radii[i]);
        }
        return Math.Max(min_val,position.Length() - 0.5F*200.0F);
        // return min_val;
    }

    private Vector3 GetDensityNormal(Vector3 position)
    {
        float delta = 0.0003F;
        float base_density = GetDensity(position);
        return new Vector3(
            GetDensity(position+ new Vector3(delta,0,0))-base_density,
            GetDensity(position+ new Vector3(0,delta,0))-base_density,
            GetDensity(position+ new Vector3(0,0,delta))-base_density
        ).Normalized();
    }

