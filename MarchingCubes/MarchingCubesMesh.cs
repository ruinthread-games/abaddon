        if ((edgeTable[cubeindex] & 1024) != 0)
            vertlist[10] = VertexInterp(isolevel,grid.p[2],grid.p[6],grid.val[2],grid.val[6]);
        if ((edgeTable[cubeindex] & 2048) != 0)
            vertlist[11] = VertexInterp(isolevel,grid.p[3],grid.p[7],grid.val[3],grid.val[7]);

        /* Create the triangle */
        ntriang = 0;
        for (i=0;triTable[cubeindex,i]!=-1;i+=3) {            
            triangles[ntriang].p = new Vector3[3];
            triangles[ntriang].p[0] = vertlist[triTable[cubeindex,i  ]];
            triangles[ntriang].p[1] = vertlist[triTable[cubeindex,i+1]];
            triangles[ntriang].p[2] = vertlist[triTable[cubeindex,i+2]];
            ntriang++;
        }

        return(ntriang);
    }

    /*
    Linearly interpolate the position where an isosurface cuts
    an edge between two vertices, each with their own scalar value
    */
    private Vector3 VertexInterpAlt(float isolevel, Vector3 p1, Vector3 p2, float valp1, float valp2)
    {
        float mu;
        Vector3 p;

        if (Math.Abs(isolevel-valp1) < 0.00001)
            return(p1);
        if (Math.Abs(isolevel-valp2) < 0.00001)
            return(p2);
        if (Math.Abs(valp1-valp2) < 0.00001)
            return(p1);
        mu = (isolevel - valp1) / (valp2 - valp1);
        p.x = p1.x + mu * (p2.x - p1.x);
        p.y = p1.y + mu * (p2.y - p1.y);
        p.z = p1.z + mu * (p2.z - p1.z);

        return(p);
    }

    private Vector3 VertexInterp(float isolevel, Vector3 p1, Vector3 p2, float valp1, float valp2)
    {
        // if (CompareVectors(p1,p2))
        if (CompareVectors(p2,p1))
        {
            Vector3 temp_p;
            temp_p = p1;
            p1 = p2;
            p2 = temp_p;  
            float temp_val; 
            temp_val = valp1; 
            valp1 = valp2;
            valp2 = temp_val;
        }

        Vector3 p;
        if(Math.Abs(valp1 - valp2) > 0.00001)
        {
            // p = p1 + (isolevel - valp1)*(p2 - p1)/(valp2 - valp1);
            float mu = (isolevel - valp1) / (valp2 - valp1);            
            p = p1 + mu*(p2-p1);
        }
        else 
        {
            p = p1;
        }
        return p;
    }

    private bool CompareVectors(Vector3 first, Vector3 second)
    {
        if (first.x < second.x)
            return true;
        else if (first.x > second.x)
            return false;

        if (first.y < second.y)
            return true;
        else if (first.y > second.y)
            return false;

        if (first.z < second.z)
            return true;
        else if (first.z > second.z)
            return false;

        return false;
    }
